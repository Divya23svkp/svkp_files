package BasicsOfJava;

public class BitwiseOperator {

	public static void main(String[] args) {
		int a=5;
		int b=3;
		
		//bitwise AND
		System.out.println("(a&b):"+(a&b));
		
		//bitwise OR
		System.out.println("(a|b):"+(a|b));
		
		//bitwise XOR
		System.out.println("(a^b):"+(a^b));
		
		//bitwise NOt
		System.out.println("~a:"+~a);
		
		//Left shift
		System.out.println("(a<<3):"+(a<<3));
		
		//Right shift
		System.out.println("(a>>2):"+(a>>2));




		
		



	}

}
